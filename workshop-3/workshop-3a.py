# WORKSHOP 3A - ปฏิบัติการเก็บค่าคุ้มครอง (For Loop)

# 1. รับจำนวนร้านค้าจากบอส (แปลงเป็น int เพื่อใช้บอกจำนวนรอบลูป)
num_shops = int(input("บอสสั่งให้ไปเก็บส่วยกี่ร้าน? : "))
total_money = 0  # ตัวแปรเก็บยอดรวม

# 2. วนลูปตามจำนวนร้านที่ระบุ
for i in range(num_shops):
    # รับค่าเงินและแปลงเป็นทศนิยม
    money = float(input(f"เก็บเงินจากร้านที่ {i+1} ได้เท่าไหร่? : "))
    total_money += money  # บวกทบเข้าไปในยอดรวม

print(f"\n[รายงาน] บอสครับ! เก็บครบ {num_shops} ร้านแล้ว ได้เงินมาทั้งหมด: {total_money} บาท")
